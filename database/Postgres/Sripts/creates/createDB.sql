-- Database: book_crossing

-- DROP DATABASE IF EXISTS book_crossing;

CREATE DATABASE book_crossing
    WITH
    OWNER = postgres
    ENCODING = 'UTF8'
    LC_COLLATE = 'ru_RU.UTF-8'
    LC_CTYPE = 'ru_RU.UTF-8'
    LOCALE_PROVIDER = 'libc'
    TABLESPACE = pg_default
    CONNECTION LIMIT = -1
    IS_TEMPLATE = False;

GRANT TEMPORARY, CONNECT ON DATABASE book_crossing TO PUBLIC;

GRANT ALL ON DATABASE book_crossing TO postgres;

